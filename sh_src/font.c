static int char_to_idx(char c) {
    if (c >= 'A' && c <= 'Z') return c - 'A';
    if (c >= 'a' && c <= 'z') return c - 'a';   /* fold case */
    if (c >= '0' && c <= '9') return 26 + (c - '0');
    switch (c) {
    case ' ': return 36;
    case '>': return 37;
    case '+': return 38;
    case '-': return 39;
    case '.': return 40;
    case ':': return 41;
    case '|': return 42;
    case '%': return 43;
    default:  return 36;   /* unknowns render as space */
    }
}

void font_draw_char(uint8_t *fb, int x, int y, char c, uint8_t color) {
    const uint8_t *g = glyphs[char_to_idx(c)];
    for (int row = 0; row < 8; row++) {
        uint8_t bits = g[row];
        uint8_t *p = fb + (y + row) * SCREEN_W + x;
        if (bits & 0x80) p[0] = color;
        if (bits & 0x40) p[1] = color;
        if (bits & 0x20) p[2] = color;
        if (bits & 0x10) p[3] = color;
        if (bits & 0x08) p[4] = color;
        if (bits & 0x04) p[5] = color;
        if (bits & 0x02) p[6] = color;
        if (bits & 0x01) p[7] = color;
    }
}

void font_draw_string(uint8_t *fb, int x, int y, const char *s, uint8_t color) {
    while (*s) {
        font_draw_char(fb, x, y, *s, color);
        x += 8;
        s++;
    }
}
